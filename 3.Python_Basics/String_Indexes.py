# String Indexes

selfish = "0123456789"

print(selfish[0])
print(selfish[8])
print(selfish)

print(selfish[0:2])
print(selfish[0:9])
print(selfish[0:9:2])

print(selfish[1:])
print(selfish[:5])
print(selfish[::1])
print(selfish[-1])
print(selfish[-3])
print(selfish[::-1])
print(selfish[::-2])
